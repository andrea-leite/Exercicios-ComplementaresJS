// Crie uma função chamada `calcularAreaRetangulo` que receba dois parâmetros: `largura` e
// `altura`. A função deve retornar a área (largura * altura) e exibir o resultado no console

function calcularAreaRetangulo(largura, altura){
    return largura * altura
}
